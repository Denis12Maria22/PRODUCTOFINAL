//ejercicio 4 calculadora if_else

let numero1 = 10;
let numero2 = 5;
let operador = "-";

if (operador === "+") {
    console.log(numero1 + numero2);
} else if (operador === "*") {
    console.log(numero1 * numero2);
} else if (operador === "/") {
    console.log(numero1 / numero2);
} else if (operador === "-") {
    console.log(numero1 - numero2);
}